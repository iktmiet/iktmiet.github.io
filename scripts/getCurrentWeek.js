// –°–∫—Ä–∏–ø—Ç –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–π –Ω–µ–¥–µ–ª–∏ –ø–æ –º–æ—Å–∫–æ–≤—Å–∫–æ–º—É –≤—Ä–µ–º–µ–Ω–∏
function getCurrentWeek() {
    var startDate = new Date("2024-09-02"); // –î–∞—Ç–∞ –Ω–∞—á–∞–ª–∞ –æ—Ç—Å—á—ë—Ç–∞
    var currentDate = new Date(); // –¢–µ–∫—É—â–∞—è –¥–∞—Ç–∞
    var moscowTimezoneOffset = 3; // UTC+3 (–ú–æ—Å–∫–æ–≤—Å–∫–æ–µ –≤—Ä–µ–º—è)

    // –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–µ–µ –º–æ—Å–∫–æ–≤—Å–∫–æ–µ –≤—Ä–µ–º—è
    var currentMoscowTime = new Date(currentDate.getTime() + moscowTimezoneOffset * 60 * 60 * 1000);

    // –í—ã—á–∏—Å–ª—è–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–µ–¥–µ–ª—å –æ—Ç –Ω–∞—á–∞–ª—å–Ω–æ–π –¥–∞—Ç—ã –≤ –º–æ—Å–∫–æ–≤—Å–∫–æ–º –≤—Ä–µ–º–µ–Ω–∏
    var weeksElapsed = Math.floor((currentMoscowTime - startDate) / (7 * 24 * 60 * 60 * 1000)) + 1;

    document.getElementById("numeratorCircle").style.display = "none";
    document.getElementById("denominatorCircle").style.display = "none";

    // –ö–æ—Ä—Ä–µ–∫—Ü–∏—è –Ω–µ–¥–µ–ª–∏ –≤ –≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ
    if (currentDate.getDay() === 0) {
        weeksElapsed += 1;
    }

    // –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ç–∏–ø–∞ –Ω–µ–¥–µ–ª–∏ –ø–æ –æ—Å—Ç–∞—Ç–∫—É –æ—Ç –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 4
    var weekType;
    switch (weeksElapsed % 4) {
        case 1:
            weekType = '–ß–∏—Å–ª–∏—Ç–µ–ª—å [1]';
            document.getElementById("numeratorCircle").style.display = 'block';
            break;
        case 2:
            weekType = '–ó–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å [1]';
            document.getElementById("denominatorCircle").style.display = 'block';
            break;
        case 3:
            weekType = '–ß–∏—Å–ª–∏—Ç–µ–ª—å [2]';
            document.getElementById("numeratorCircle").style.display = 'block';
            break;
        case 0:
            weekType = '–ó–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å [2]';
            document.getElementById("denominatorCircle").style.display = 'block';
            break;
    }

    // –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç —ç–ª–µ–º–µ–Ω—Ç–∞ —Å –Ω–æ–º–µ—Ä–æ–º –Ω–µ–¥–µ–ª–∏
    if (currentDate.getDay() === 0) {
        document.getElementById("weekLegend").innerHTML = '<b style="color: cyan">–ó–∞–≤—Ç—Ä–∞ ' + weeksElapsed + "-—è –Ω–µ–¥–µ–ª—è</b> | <b>" + weekType + "</b>";
    } else {    
        document.getElementById("weekLegend").textContent = "üìÜ " + weeksElapsed + " –Ω–µ–¥–µ–ª—è | " + weekType;
    }

    // –í—ã–∑–æ–≤ changeSchedule –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ç–∏–ø–∞ –Ω–µ–¥–µ–ª–∏
    if (weekType.includes('–ß–∏—Å–ª–∏—Ç–µ–ª—å')) {
        changeSchedule('numerator');
    } else {
        changeSchedule('denominator');
    }
}